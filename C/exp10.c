#include<stdio.h>
#include<stdlib.h>
int main()
{
	int a[100],b[100],n,i,j,temp,c;
	scanf("%d",&n);
	for(i=0;i<n;i++)
	{
		scanf("%d",&a[i]);
	}
	for(i=0;i<n;i++)
	{
		b[i]=a[i];
	}
	for(i=0;i<n;i++)
	{
		for(j=0;j<n;j++)
		{
			if(a[i]<a[j])
			{
				temp=a[i];
				a[i]=a[j];
				a[j]=temp;
			}
		}
	}
	c=0;
	for(i=0;i<n;i++)
	{
		j=0;
		for(j=0;j<n;j++)
		{
			if(a[i]==b[j])
			{
				c++;
				b[j]=c;
			}
		}
	}
	printf("\n");
	for(i=0;i<n;i++)
	{
		printf("%d ",b[i]);
	}
}
